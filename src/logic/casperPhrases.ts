export const phrases: Record<'green' | 'yellow' | 'red', string[]> = {
  green: [
    'Тіло готове.\nВиходимо в ритм.',
    'Каспер поруч.\nЙдемо спокійно, без ривків.',
    'Добрий стан — добрий крок сьогодні.',
  ],
  yellow: [
    'Сьогодні не треба рекорду.\nДостатньо повернути ритм.',
    'Тримай ритм.\nНе тисни на себе.',
    'Невелика втома — це нормально.\nКрок маленький теж рахується.',
  ],
  red: [
    'Сьогодні краще відпочити.\nПауза — це теж частина прогресу.',
    'Пропуск — не провал.\nПросто повертаємось до маршруту завтра.',
  ],
}

export function pickPhrase(level: 'green' | 'yellow' | 'red'): string {
  const arr = phrases[level]
  return arr[Math.floor(Math.random() * arr.length)]
}
